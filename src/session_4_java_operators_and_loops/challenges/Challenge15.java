package session_4_java_operators_and_loops.challenges;

public class Challenge15 {

    public static void main(String[] args) {
        int total = 100;

        total -= 20;
        System.out.println("After decreasing by 20, total = " + total);

        total *= 2;
        System.out.println("After multiplying by 2, total = " + total);
    }
}